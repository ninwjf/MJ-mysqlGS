        public static byte GetCRC8(byte[] buffer)
        {
            return GetCRC8(buffer, 0, buffer.Length);
        }

        /// <summary>
        /// 计算Crc8校验值
        /// </summary>
        /// <param name="buffer">数据包</param>
        /// <param name="off">起始下标</param>
        /// <param name="len">长度</param>
        /// <returns></returns>
        public static byte GetCRC8(byte[] buffer, int off, int len)
        {
            byte crc = 0;
            if (buffer == null)
            {
                throw new ArgumentNullException("buffer");
            }
            if (off < 0 || len < 0 || off + len > buffer.Length)
            {
                throw new ArgumentOutOfRangeException();
            }

            for (int i = 0; i < len; i++)
            {
                crc = CRC8Table[crc ^ buffer[off + i]];
            }
            return Convert.ToByte(255 - crc);
        }
